<template>
  <vs-dialog
    v-model="active"
    id="suggestionsPopup"
    class="content-popup"
    style="z-index: 1000000000;"
    scroll
    :full-screen="!isLargeScreen"
    prevent-close
    overflow-hidden
  >
    <template #header>
      <div class="pt-10">
        <h4 class="not-margin text-title text-4xl"><b>Posted</b> Notes</h4>
      </div>
    </template>

    <div
      class="con-form md:p-4 lg:p-8 p-2 flex vx-row w-full justify-evenly overflow-x-hidden"
    >
      <vs-input
        v-model="title"
        placeholder="You should sell chocolate"
        label="Title"
        class="block mb-6 w-6 mt-6"
        width="w-6"
      >
        <template #icon>
          <i class="bx bx-highlight" primary></i>
        </template>
      </vs-input>

      <VsTextarea
        v-model="contents"
        placeholder="Sourced from Switzerland, shipped and packaged in Columbia, distributed and sold in the U.S."
        class="block"
        height="20rem"
        label="NOTABLE Content"
      >
      </VsTextarea>
      <VsUpload
        :show-upload-button="false"
        multiple
        text="Upload Image(s)"
        accept="image/*"
        ref="postImageUpload"
      />
    </div>

    <template #footer>
      <div class="footer-dialog vx-row justify-center md:pb-8 md:px-12 px-2">
        <vs-button
          class="md:w-1/2 w-full"
          warn
          :disabled="formErrors"
          @click="PreviewNote()"
        >
          <div class="text-xl p-2 font-bold lg:text-2xl" style="">
            PREVIEW NOTE
          </div>
        </vs-button>
      </div>
    </template>
  </vs-dialog>
</template>

<script lang="ts">
import { Component, Vue, Prop, mixins, Watch } from 'nuxt-property-decorator'

import { suggestionsStore, notesStore, windowStore } from '~/store'
import {
  NestedSubjectList,
  SubjectGroup_O,
  Subject_O,
  SubjectIconList,
  Grade_O,
  GradeList
} from '~/types/subjects'

import ValidateImage from '~/mixins/ValidateImageMixin'
import { Note } from '~/types/notes'
import VsTextarea from '~/components/VsTextarea.vue'
import VsUpload from '~/components/VsUpload.vue'

import { authStore } from '~/store'

interface imageSrc {
  error: boolean
  orientation: 'w' | 'l'
  percent: number | string | null
  remove: boolean | null
  src: string | null
}

@Component<PostNotesModal>({
  components: {
    VsTextarea,
    VsUpload
  }
})
export default class PostNotesModal extends mixins(ValidateImage) {
  subjectSelect: Subject_O | '' = ''
  gradeSelect: Grade_O | '' = ''

  @Watch('IsReset')
  onResetChanged(value : boolean, oldVal : boolean)
  {
    if(value)
    {
      this.ClearFields();
      notesStore.SET_RESET(false);
    }
  }

  get IsReset()
  {
    return notesStore.IsReset;
  }

  readonly GradeList = GradeList.filter(v=>v!=='ALL');
  Cancel() {}
  // make this a mixin
  getIcon(subject: SubjectGroup_O | Subject_O) {
    return SubjectIconList[subject]
  }
  readonly SubjectGroupList = NestedSubjectList
  get active() {
    return notesStore.NotesModuleOpen
  }
  set active(value: boolean) {
    notesStore.ToggleNotesModule(value)
  }
  title = ''
  contents = ''
  ClearFields()
  {
    this.title=  this.contents = this.subjectSelect = this.gradeSelect = '';
    this.srcs?.forEach(src => src.remove = true);
  }
  get isLargeScreen() {
    return windowStore.isLargeScreen
  }

  get imageRefs() {
    return (this.$refs.postImageUpload as Vue & {
      filesx: File[]
      srcs: imageSrc[]
      itemRemove: any[]
    }|undefined)?.filesx;
  }
  get srcs()
  {
    return (this.$refs.postImageUpload as Vue & {
      filesx: File[]
      srcs: imageSrc[]
      itemRemove: any[]
    } | undefined)?.srcs;
  }
  async PreviewNote() {
    const refs = this.$refs.postImageUpload as Vue & {
      filesx: File[]
      srcs: imageSrc[]
      itemRemove: any[]
    } | undefined
    const itemRemove = refs?.itemRemove
    const srcs = refs?.srcs.filter((src) => !src.remove).map((src) => src.src!)
    const postImageUpload = refs?.filesx
    
    if (this.formErrors) {
      this.$vs.notification({
        color: 'danger',
        title: 'Fill Out All Fields!'
      })
      return
    }

    const previewNote = new Note({
      title: this.title,
      uid: authStore.user?.uid!,
      userDisplayName: authStore.user?.displayName!,
      userPhotoUrl: authStore.user?.photoURL!,
      createdAt: new Date(),
      upVotes: 0,
      views: 0,
      subject: this.subjectSelect as Subject_O,
      grade: this.gradeSelect as Grade_O,
      contents: this.contents,
      images: srcs
    })

    notesStore.SET_UPLOAD_IMAGES(postImageUpload);
    notesStore.SetPreviewNote(previewNote)
    notesStore.TogglePreviewModal(true)
  }

  set state(value: boolean) {
    this.title = this.contents = ''
    this.active = false
  }
  get state() {
    return this.active
  }

  get formErrors() {
    return (
      !this.title ||
      this.subjectSelect == '' ||
      this.gradeSelect == '' ||
      !this.contents ||

      (this.imageRefs && this.imageRefs.filter(image => this.validateImageType(image)).length < this.imageRefs.length)
    )
  }
}
</script>


